#include <iostream>
#include <string>
#include <stack>
#include <algorithm>

using namespace std;

int longestValidParentheses(string s)
{
    stack<int> stk;
    int ansMax = 0, rIdx = -1;

    for (int i = 0; i < s.length(); i++) {
        if (s[i] == '(') {
            stk.push(i);
        } else {
            if (!stk.empty()) {
                stk.pop();
                ansMax = max(ansMax, i - (stk.empty() ? rIdx : stk.top()));
            } else {
                rIdx = i;
            }
        }
    }

    return ansMax;
}

